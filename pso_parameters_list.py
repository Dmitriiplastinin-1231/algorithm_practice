"""
Список коэффициентов и параметров роевого алгоритма (PSO)
с модификацией коэффициента сжатия, доступных для параметрических исследований.

Исходный класс: PSO (pso.py)
Точка запуска:  roevoy.py

==========================================================================
ПАРАМЕТРЫ, ДОСТУПНЫЕ ДЛЯ ПАРАМЕТРИЧЕСКИХ ИССЛЕДОВАНИЙ
==========================================================================

1. c1 — когнитивный коэффициент (коэффициент личного обучения)
   ─────────────────────────────────────────────────────────────
   Роль:        Определяет, насколько сильно частица тяготеет к своей
                собственной лучшей найденной позиции (personal best).
   Формула:     cognitive = c1 * r1 * (best_position_i - position_i)
   По умолчанию: 1.49618 (классический режим), 2.05 (режим сжатия)
   Ограничение: c1 + c2 > 4 (при use_constriction=True)
   Диапазон для исследования: [1.0 … 2.8]

2. c2 — социальный коэффициент (коэффициент глобального обучения)
   ─────────────────────────────────────────────────────────────────
   Роль:        Определяет, насколько сильно частица тяготеет к глобальной
                лучшей позиции всего роя (global best).
   Формула:     social = c2 * r2 * (global_best_position_i - position_i)
   По умолчанию: 1.49618 (классический режим), 2.05 (режим сжатия)
   Ограничение: c1 + c2 > 4 (при use_constriction=True)
   Диапазон для исследования: [1.0 … 2.8]

3. kappa (κ) — масштабирующий параметр коэффициента сжатия
   ──────────────────────────────────────────────────────────
   Роль:        Управляет «жёсткостью» коэффициента сжатия χ.
                При κ = 1 получается стандартное сжатие Клерка–Кеннеди.
   Формула:     χ = (2 * κ) / |2 − φ − √(φ² − 4φ)|,  φ = c1 + c2
   По умолчанию: 1.0
   Диапазон для исследования: [0.5 … 1.0]

4. chi (χ) — коэффициент сжатия (производный параметр)
   ───────────────────────────────────────────────────────
   Роль:        Масштабирует обновление скорости, обеспечивая сходимость роя.
                Вычисляется автоматически из c1, c2 и kappa; напрямую не задаётся.
   Формула:     χ = (2 * κ) / |2 − φ − √(φ² − 4φ)|
   Типичное значение при c1 = c2 = 2.05, κ = 1: χ ≈ 0.7298
   Примечание:  Исследуется косвенно — через изменение c1, c2 или kappa.

5. phi (φ = c1 + c2) — суммарный коэффициент обучения (производный параметр)
   ─────────────────────────────────────────────────────────────────────────────
   Роль:        Определяет «суммарное притяжение» частицы. Влияет на χ.
   Ограничение: φ > 4 (необходимо для существования χ)
   Типичное значение: 4.1 (φ = 2.05 + 2.05)
   Примечание:  Исследуется косвенно — через изменение c1 и/или c2.

6. num_particles — количество частиц в рое
   ──────────────────────────────────────────
   Роль:        Задаёт размер популяции (роя). Чем больше частиц, тем полнее
                исследуется пространство поиска, но растут вычислительные затраты.
   По умолчанию: 30 (в PSO), 40 (в roevoy.py)
   Диапазон для исследования: [10 … 100]

7. max_iterations — максимальное количество итераций
   ─────────────────────────────────────────────────────
   Роль:        Критерий остановки алгоритма. Определяет «бюджет» вычислений.
   По умолчанию: 100
   Диапазон для исследования: [50 … 500]

8. w — инерционный вес (только в классическом режиме, use_constriction=False)
   ─────────────────────────────────────────────────────────────────────────────
   Роль:        Определяет инерцию скорости частицы. В режиме с коэффициентом
                сжатия (use_constriction=True) не используется отдельно —
                функцию инерции берёт на себя χ.
   Формула:     velocity_i = w * velocity_i + cognitive + social
   По умолчанию: 0.7298
   Диапазон для исследования: [0.4 … 0.9]
   Примечание:  Не актуален при исследовании режима с коэффициентом сжатия.

==========================================================================
ИТОГОВАЯ ТАБЛИЦА ДЛЯ РОЕВОГО АЛГОРИТМА С КОЭФФИЦИЕНТОМ СЖАТИЯ
==========================================================================

Параметр        Тип         Знач. по умолч.   Ограничение       Исследуется
─────────────────────────────────────────────────────────────────────────────
c1              прямой      2.05              c1+c2 > 4         да
c2              прямой      2.05              c1+c2 > 4         да
kappa (κ)       прямой      1.0               (0, 1]            да
chi (χ)         производный ≈ 0.7298          > 0               косвенно
phi (φ)         производный ≈ 4.1             > 4               косвенно
num_particles   прямой      40                ≥ 1               да
max_iterations  прямой      100               ≥ 1               да
w               прямой      0.7298            —                 нет (только classic)
"""


# ─── Значения параметров по умолчанию (из roevoy.py / pso.py) ───────────────

PSO_DEFAULT_PARAMS = {
    "c1":             2.05,    # когнитивный коэффициент (режим сжатия)
    "c2":             2.05,    # социальный коэффициент  (режим сжатия)
    "kappa":          1.0,     # масштабирующий параметр χ
    "num_particles":  40,      # число частиц
    "max_iterations": 100,     # число итераций
    "w":              0.7298,  # инерционный вес (только classic-режим)
}

# ─── Диапазоны для параметрических исследований ──────────────────────────────

PSO_STUDY_RANGES = {
    "c1":             (1.0, 2.8),   # при условии c1 + c2 > 4
    "c2":             (1.0, 2.8),   # при условии c1 + c2 > 4
    "kappa":          (0.5, 1.0),
    "num_particles":  (10, 100),
    "max_iterations": (50, 500),
}


if __name__ == "__main__":
    import math

    print("=" * 60)
    print("  PSO с коэффициентом сжатия — параметры для исследования")
    print("=" * 60)

    print("\nПараметры по умолчанию (roevoy.py / pso.py):")
    for name, val in PSO_DEFAULT_PARAMS.items():
        print(f"  {name:<20} = {val}")

    c1 = PSO_DEFAULT_PARAMS["c1"]
    c2 = PSO_DEFAULT_PARAMS["c2"]
    kappa = PSO_DEFAULT_PARAMS["kappa"]
    phi = c1 + c2
    chi = (2.0 * kappa) / abs(2.0 - phi - math.sqrt(phi ** 2 - 4.0 * phi))

    print(f"\nПроизводные коэффициенты при c1={c1}, c2={c2}, kappa={kappa}:")
    print(f"  {'phi (φ = c1+c2)':<20} = {phi:.6f}")
    print(f"  {'chi (χ)':<20} = {chi:.6f}")

    print("\nДиапазоны для параметрических исследований:")
    for name, (lo, hi) in PSO_STUDY_RANGES.items():
        print(f"  {name:<20}: [{lo} … {hi}]")
